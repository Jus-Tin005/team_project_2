Mathematics (from Ancient Greek μάθημα; máthēma: 'knowledge, study, learning') is an area of knowledge that includes such topics as numbers (arithmetic and number theory), formulas and related structures (algebra),[2] shapes and the spaces in which they are contained (geometry), and quantities and their changes (calculus and analysis).[3][4][5] Most mathematical activity involves the use of pure reason to discover or prove the properties of abstract objects, which consist of either abstractions from nature or—in modern mathematics—entities that are stipulated with certain properties, called axioms. A mathematical proof consists of a succession of applications of some deductive rules to already known results, including previously proved theorems, axioms and (in case of abstraction from nature) some basic properties that are considered as true starting points of the theory under consideration.

Mathematics is used in science for modeling phenomena, which then allows predictions to be made from experimental laws. The independence of mathematical truth from any experimentation implies that the accuracy of such predictions depends only on the adequacy of the model. Inaccurate predictions, rather than being caused by incorrect mathematics, imply the need to change the mathematical model used. For example, the perihelion precession of Mercury could only be explained after the emergence of Einstein's general relativity, which replaced Newton's law of gravitation as a better mathematical model.

Mathematics is essential in the sciences, engineering, medicine, finance, computer science and the social sciences. Some areas of mathematics, such as statistics and game theory, are developed in close correlation with their applications and are often grouped under applied mathematics. Other mathematical areas are developed independently from any application (and are therefore called pure mathematics), but practical applications are often discovered later. A fitting example is the problem of integer factorization, which goes back to Euclid, but which had no practical application before its use in the RSA cryptosystem (for the security of computer networks).

Historically, the concept of a proof and its associated mathematical rigour first appeared in Greek mathematics, most notably in Euclid's Elements. Since its beginning, mathematics were essentially divided into geometry, and arithmetic (the manipulation of natural numbers and fractions), until the 16th and 17th centuries, when algebra[b] and infinitesimal calculus were introduced as new areas of the subject. Since then, the interaction between mathematical innovations and scientific discoveries has led to a rapid increase in the development of mathematics. At the end of the 19th century, the foundational crisis of mathematics led to the systematization of the axiomatic method. This gave rise to a dramatic increase in the number of mathematics areas and their fields of applications. An example of this is the Mathematics Subject Classification, which lists more than 60 first-level areas of mathematics.
